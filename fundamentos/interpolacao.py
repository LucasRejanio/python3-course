# Interpolação

# Substituição de variáveis
nome = 'Ana'
idade = 30

# Metodo mais antigo 
print('Nome: %s Idade: %d' % (nome, idade))

# Metodo para Python com a versão menor que 3.6
print('Nome: {0} Idade: {1}'.format(nome,idade))

# Metodo para Versão do Python à partir da 3.6
print(f'Nome: {nome} Idade: {idade}')
